    private void boton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_boton1ActionPerformed
        
        code = (int) (Math.random() * 10000);
        producto = campo2.getText();
        precio = Integer.parseInt(campo3.getText());
        cantidad = Integer.parseInt(campo4.getText());
        
        objectAction guardar = new objectAction(this.code,this.producto, this.precio, this.cantidad);
        guardar.save();
        
        itemProvider.loadItemTable(jTable1);
        
        clearForm();
        
    }//GEN-LAST:event_boton1ActionPerformed

    private void boton2ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_boton2ActionPerformed
        
        
        id = Integer.parseInt(campo1.getText());
        
        objectAction actualizar = new objectAction(id,campo2.getText(), Integer.parseInt(campo3.getText()), Integer.parseInt(campo4.getText()));
        actualizar.update();
        
        itemProvider.loadItemTable(jTable1);
        
        clearForm();
        
    }//GEN-LAST:event_boton2ActionPerformed

    private void boton3ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_boton3ActionPerformed
        
        id = Integer.parseInt(campo1.getText());
        
        objectAction eliminar = new objectAction(id,this.producto, this.precio, this.cantidad);
        eliminar.delete();
        
        itemProvider.loadItemTable(jTable1);
        
        clearForm();
        
    }//GEN-LAST:event_boton3ActionPerformed

