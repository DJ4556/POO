    private void jTable1MouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_jTable1MouseClicked
        
        int selection = jTable1.getSelectedRow();
        
        campo1.setText(jTable1.getValueAt(selection, 0) + "");
        campo2.setText(jTable1.getValueAt(selection, 1) + "");
        campo3.setText(jTable1.getValueAt(selection, 2) + "");
        campo4.setText(jTable1.getValueAt(selection, 3) + "");
        
    }//GEN-LAST:event_jTable1MouseClicked

